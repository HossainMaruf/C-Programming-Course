#include <stdio.h>
void main() {
    printf("Sum = %d\n", 10+3); // 13
    printf("Minus = %d\n", 10-3); // 7
    printf("Multiplication = %d\n", 10*3); // 30
    printf("Quotient = %d\n", 10/3); // 3
    printf("Remainder = %d\n", 10%3); // 1 (Mod = Modulus)


    printf("Sum = %f\n", 10.5+3.5);
    printf("Minus = %f\n", 10.5-3.5);
    printf("Multiplication = %f\n", 10.5*3.5);
    printf("Division = %f\n", 10.5/3.5);
    // printf("Remainder = %d\n", 10.5%3.5); // ERROR
}