#include <stdio.h>
void main() {
    // FOR INTEGER
    printf("Sum = %d\n", 10 + 3);
    printf("Sum = %d\n", 10 - 3);
    printf("Sum = %d\n", 10 * 3);
    printf("Sum = %d\n", 10 / 3);
    printf("Sum = %d\n", 10 % 3);
    
    // FOR FLOATING POINT
    printf("Sum = %f\n", 10.3 + 3.3);
    printf("Sum = %f\n", 10.3 - 3.3);
    printf("Sum = %f\n", 10.3 * 3.3);
    printf("Sum = %f\n", 10.3 / 3.3);
    // printf("Sum = %d\n", 10.3 % 3.3); // ERROR
}